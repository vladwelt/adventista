<h2>Nuestra visión</h2>
<p>Nuestra vision es hacer manifiesto el Reino de Dios a las naciones del mundo
y expresar la plenitud del Evangelio de Nuestro Señor Jesucristo. Con las
Sagradas Escrituras y las Sagradas Tradiciones de la Iglesia como nuestra guía,
buscamos la experiencia de la total naturaleza de la viviente, histórica Iglesia
Católica en su comunión sacramental con un Dios personal, su gozo carismático en
el Espíritu, y su celo evangélico para compartir nuestra bendita esperanza con
la gente.</p>

<h2>Nuestra misión</h2>
<p>San Pablo en la carta de primera de Corintios 3:6, dice: "Yo planté, Apolos
regó; pero el crecimiento lo ha dado Dios".</p>
<p>Nuestra misión está enfocada en dos declaraciones de Jesús: el Gran
Mandamiento y la Gran Comisión. Primera declaración, El dijo: "Amarás al Señor
tu Dios con todo tu corazón, y con toda tu alma, y con toda tu mente, y amarás a
tu prójimo como a ti mismo (Mateo 22:37-40). Segunda declaración, El también
dijo: "Por tanto, id, y haced discípulos a todas las naciones, bautizándolos en
el nombre del Padre, y del Hijo, y del Espíritu Santo; enseñándoles que guarden
todas las cosas que les he mandado".</p>

<h2>La iglesia</h2>
<p>La Iglesia es Universal, fundada por el mismo Jesucristo. Es la sociedad
visible para perpetuar en la tierra su triple misión de enseñar, regir y
santificar a los hombres. Ser católico es estar en comunión con Cristo, no en
comunión con alguna iglesia en particular.</p>
<p>Hace aproximadamente 2 mil años, Jesucristo fundó su Iglesia, UNA que
perpetuará hasta el fin del mundo, (1 Timoteo 3; 14 Mateo 16,18; Jn.10, 16). Es
la Iglesia que por su historia y por la transmisión de la autoridad, procede de
la Línea de los Apóstoles; la que no enseña error, ni puede ser destruida. A
nuestra Iglesia se le da el nombre de Católica porque esto significa Universal y
en comunión con Cristo. ésta Iglesia está destinada a todas las personas, de
todos los tiempos y naciones porque transmite el mensaje y las enseñanzas de
Jesús. La Iglesia tal como la fundó Jesucristo, tiene cuatro notas
características, que son propiedades esenciales y que todas juntas son
exclusivas y manifestativas de la Verdadera Iglesia de Jesucristo.</p>

<p>Estas señales son:</p>
<ul>
    <li>Unidad, (con la Santísima Trinidad)</li>
    <li>Santidad, (porque está ungida por el Espíritu Santo)</li>
    <li>Catolicidad (porque está en comunión con Cristo)</li>
    <li>y Apostolicidad (porque sigue las enseñas infalibles de los
        Apostoles.</li>
</ul>

<h2>Unidad</h2>
<p>Con la santísima trinidad e iglesias de la ortodoxia de la fe, o sea de
doctrinas justas y sanas.</p>

<h2>Santidad</h2>
<p>El primer objetivo de la venida de Cristo fue Santificar o dar la vida de
gracia, lo cual hace la Iglesia por medio de los sacramentos, santa en sus
medios y dotada con dones milagrosos. (Tito 2,14-15; Efesios 5,25-27) Cada uno
de los miembros de la Iglesia están llamados a ser santos.</p>

<h2>Catolicidad</h2>
<p>Jesucristo fundó su Iglesia para todos los hombres y para todos los tiempos;
(Marcos 16,15; Mateo 28,19-20) la fundó Universal, sin deterioro de su unidad e
indisoluble, para difundirse por todo el mundo (Génesis 22,18; Lucas 24,46-48).
</p>

<h2>Apostilicidad</h2>
<p>La Iglesia verdadera tiene que entroncar con los Apóstoles, en quienes Cristo
fundó su Iglesia. A ellos dio su potestad: “Yo os envío a vosotros", Juan 20,21;
Lucas 10,16. Debe ser apostólica en su doctrina, autoridad y miembros
(Mateo 28,18-20). Esto solo lo cumple la Iglesia Católica y Apostólica de
Jesucristo.</p>

<h2>El cuerpo místico de cristo</h2>
<p>El Espíritu Santo habita en todos los miembros de la Iglesia y los asiste con
su gracia, según la diversidad de oficios y tareas. De éste modo guía a la
Iglesia, le infunde vida y le asegura la unidad. "como Cristo es la cabeza de la
Iglesia, el Espíritu Santo es su alma". Todos los que han recibido el Bautismo y
tienen fe en Cristo pertenecen a la Iglesia de Cristo (1 Corintios 12,12-27).
Por eso, llamamos a la Iglesia el Cuerpo Místico de Cristo, pues Cristo es la
cabeza. Todos somos sus miembros como El mismo lo dijo con otra comparación: "Yo
soy la vid y vosotros los sarmientos". Como los sarmientos reciben la sabia de
la vid y gracias a ella producen las uvas, así nosotros recibimos de Jesucristo
la Gracia.</p>

<h2>Somos parte de la iglesia</h2>
<p>La Iglesia no sólo la forma una Jerarquía, somos nosotros mismos, el Pueblo
de Dios; cada uno de nosotros es una célula del Cuerpo Místico de Cristo, con
nuestra virtud colaboramos a su vitalidad y con nuestros pecados, además de
convertirnos en células muertas, entorpecemos la vida de las otras células,
nuestros hermanos. Somos libres de elegir si queremos ser la célula que mata o
la que da la vida. Sin embargo podemos poner al servicio de todos los dones
espirituales o carismas que Jesucristo nos ha dejado (1 Corintios 12,1-11). La
palabra Iglesia viene del griego EKLESIA que significa "Asamblea". Se ocupa
también el nombre de Iglesia para referirse al templo, pero sobre todo debemos
reconocer que es el pueblo de Dios. La Iglesia es Santa y perfecta en su
doctrina de fe y moral, pero está formada por pecadores, por eso hay cosas que
debemos mejorar dentro de ella. Debemos ser como los Santos; como San Francisco
de Asís, quien reformó la Iglesia con amor y formando siempre parte de ella.</p>

<h2>La iglesia madre</h2>
<p>Desde siempre procura nuestro bien, no sólo en esta vida, sino también en la
otra. La Iglesia es nuestra Madre y debemos amarla y defenderla como Cristo lo
hizo y lo hace, porque es su esposa, se entregó a Sí mismo por ella
(1 Timoteo 2,5-6). La Iglesia también es hecha madre por la Palabra de Dios
fielmente recibida, por la predicación y el bautismo, engendra para la vida
nueva e inmortal a los hijos concebidos por el Espíritu Santo y nacidos de Dios.
Y también ella es virgen que custodia pura e íntegramente la fe prometida al
Esposo e imitando a la madre del Señor, por la virtud del Espíritu Santo
conserva virginalmente la fe íntegra, la sólida esperanza, la sincera
caridad.</p>
